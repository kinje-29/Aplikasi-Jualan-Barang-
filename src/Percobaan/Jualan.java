    private void txtkd_brgKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtkd_brgKeyPressed
        // TODO add your handling code here:
        try {
            String SQLB = "Select * from barang where kd_brg='" + txtkd_brg.getText() + "'";
            ps = Koneksi.KoneksiDatabase().prepareStatement(SQLB);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                txtnama_brg.setText(rs.getString("nama_brg"));
                float hrg = rs.getFloat("hrg_jual");
                txthrg_jual.setText(String.valueOf(hrg));
                txtjml.requestFocus();
            }
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_txtkd_brgKeyPressed

