    private void NoTrans() {
        try {
            String SQLB = "select max(no_trans)as nt from jual";
            ps = Koneksi.KoneksiDatabase().prepareStatement(SQLB);
            ResultSet rs = ps.executeQuery(SQLB);
            if (rs.next()) {
                long nomor = rs.getInt("nt");
                nomor = nomor + 1;
                txtno_trns.setText(String.valueOf(nomor));
            } else {
                txtno_trns.setText("1");
            }
        } catch (SQLException ex) {
        }
    }

